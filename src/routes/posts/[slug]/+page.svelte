			<nav class="border-l border-gray-200 pl-4" aria-label="Table of contents">
				<ol class="m-0 grid list-none gap-2 p-0 text-sm">
					{#each data.post.toc as item (item.id)}
						<li class={item.depth === 3 ? 'pl-4' : undefined}>
							<a class="text-gray-600 no-underline hover:text-indigo-600" href={`#${item.id}`}>
								{item.text}
							</a>
						</li>
					{/each}
				</ol>
			</nav>
		</aside>
	{/if}
</main>
